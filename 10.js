"use strict";

function calc() {
	const lines = input.split('\n');

	let score1 = 0;
	const scores2 = [];

	const bracePairs = {'(': ')', '[': ']', '{': '}', '<': '>'};

	lines.forEach(line => {
		const stack = [];

		const illegal = line.split('').find(c => {
			const isOpenBrace = '([{<'.indexOf(c) >= 0;

			if (isOpenBrace) {
				stack.push(bracePairs[c]);
			} else if (stack.pop() != c) {
				return true;
			}

			return false;
		});

		if (illegal) {
			const illegalPoints = {')': 3, ']': 57, '}': 1197, '>': 25137};
			score1 += illegalPoints[illegal];
		} else {
			const closePoints = {')': 1, ']': 2, '}': 3, '>': 4};
			scores2.push(stack.reverse().reduce((a, e) =>
				a * 5 + closePoints[e], 0));
		}
	});

	const part1 = score1;

	scores2.sort((a, b) => a - b);
	const part2 = scores2[Math.floor(scores2.length / 2)];

	return part1 + ' ' + part2;
}

